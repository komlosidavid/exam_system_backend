
package inf.unideb.hu.exam.system.models.enums;

/**
 * Enumerated class for holding question types.
 */
public enum QuestionType {

    /**
     * If the question has only one option to answer.
     */
    ONE_ANSWER,

    /**
     * If the question has multiple answers.
     */
    MULTIPLE_ANSWERS,

    /**
     * Questions which are requires explanation.
     */
    EXPLANATORY_ANSWER
}
